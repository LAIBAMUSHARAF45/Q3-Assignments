# Data Types 

#string

message = "Hello,sir!"
print(message)   # <class 'str'>

# Integer

x = 7
print(x)  # <class 'int'>

# Float

y = 7.7
print(y)   # <class 'float'>

# List

fruits= ["Orange","Mango","Apple"]
print(fruits) # <class 'list'>

# Tuple

my_tuple = (1, 2, 3,)
print(my_tuple)  # <class 'tuple'>

# None Type

student = None
print(student)   # <class 'NoneType'>

# Boolean

is_active = False
print(is_active)  
is_active = True
print(is_active)  # <class 'bool'>

# Dictionary

Introduction = {'name': 'Laiba', 'age': 21}
print(Introduction)  # <class 'dict'>

# Set

my_set = {1, 2, 3, 4, 5}
print(my_set)  # <class 'set'>

# Frozenset

my_frozenset = frozenset([3, 4, 5, 6])
print(my_frozenset)   # <class 'frozenset'>


